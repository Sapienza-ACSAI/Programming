

'''LEARN RANGES'''

print(range(5)) #range(0, 5)

print(list(range(5))) # [0, 1, 2, 3, 4]

print(list(range(1,5))) # prints from element in index 1 to element in index 5 (last NOT included)

print(list(range(1,5,2))) # print from 1 to 5 (without 5) skipping 2
# so it gets [1, 3] from [1, 2, 3, 4] because it got positions 0 and 2

print(list(range(0, 50, 10))) # [0, 10, 20, 30, 40]